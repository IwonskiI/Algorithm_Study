import java.io.*;

public class Main {
    
    // BOJ 27233 - 직사각형
    public static void main(String[] args) throws Exception {
        BufferedReader br = new BufferedReader(new InputStreamReader(System.in));
        
        // 직사각형의 넓이 계산
        System.out.println(Integer.parseInt(br.readLine()) * Integer.parseInt(br.readLine()));
    }
}